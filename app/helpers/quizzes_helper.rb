# ============================================================
# Helper: クイズ表示補助
# ------------------------------------------------------------
# 目的:
# - 選択肢ラベルの安全な取得（choice1..4 を n 指定で参照）
# ============================================================
module QuizzesHelper
  # =======================
  # 選択肢のラベルを取得
  # -----------------------
  # 引数:
  #   question: QuizQuestion
  #   n       : 1..4
  # 例) <%= choice_label(@question, 2) %>
  # =======================
  def choice_label(question, n)
    question.public_send("choice#{n}")
  end
end
