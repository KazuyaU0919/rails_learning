# ============================================================
# Bookmark
# ------------------------------------------------------------
# ユーザーが PreCode をブックマークした関係を表す中間モデル。
# user_id × pre_code_id で一意。
# ============================================================

class Bookmark < ApplicationRecord
  # =======================
  # 関連
  # =======================
  belongs_to :user
  belongs_to :pre_code

  # =======================
  # バリデーション
  # =======================
  validates :user_id, uniqueness: { scope: :pre_code_id }
end
